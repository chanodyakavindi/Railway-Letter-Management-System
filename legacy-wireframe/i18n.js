function auditActionLabel(action) {
  if (!action) return "";
  const map = {
    "User created letter": "audit_created_letter",
    "User edited letter": "audit_edited_letter",
    "User completed letter": "audit_completed_letter",
    "User changed reminder": "audit_changed_reminder",
    "Secretary replied": "audit_secretary_replied",
    "User uploaded PDF": "audit_uploaded_pdf",
    "User deleted letter": "audit_deleted_letter",
    "User logged in": "audit_logged_in",
    "AI extracted letter fields": "audit_ai_extracted",
    "Export generated": "audit_export_generated",
    "Admin created user": "audit_admin_created_user",
    "Admin updated user": "audit_admin_updated_user",
  };
  if (map[action]) return t(map[action]);
  const statusMatch = String(action).match(/^User changed letter status to (.+)$/i);
  if (statusMatch) {
    const st = statusMatch[1];
    const stLabel =
      st === "Completed" ? t("stage_completed") :
      st === "Draft" ? t("stage_draft") :
      st === "NoAction" || st === "No Action Taken" ? t("stage_no_action") :
      st === "Pending" ? t("status_pending") :
      st;
    return `${t("audit_status_changed_prefix")} ${stLabel}`;
  }
  return action;
}

